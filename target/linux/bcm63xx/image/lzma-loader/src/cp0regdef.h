#define read_32bit_c0_register(reg,sel)					\
({	int __res;							\
	if (sel == 0)							\
		__asm__ __volatile__(					\
			"mfc0\t%0, " #reg "\n\t"			\
			: "=r" (__res));				\
	else								\
		__asm__ __volatile__(					\
			".set\tmips32\n\t"				\
			"mfc0\t%0, " #reg ", " #sel "\n\t"		\
			".set mips0\n\t"				\
			: "=r" (__res));				\
	__res;								\
})

